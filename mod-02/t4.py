luku1 = input('Syötä ensimmäinen luku: ')
luku2 = input('Syötä toinen luku: ')
luku3 = input('Syötä kolmas luku: ')

summa = float(luku1) + float(luku2) + float(luku3)
tulo = float(luku1) * float(luku2) * float(luku3)
keskiarvo = (float(luku1) + float(luku2) + float(luku3)) / 3

print(f'Lukujen summa: {summa}')
print(f'Lukujen tulo: {tulo}')
print(f'Lukujen keskiarvo: {keskiarvo}')